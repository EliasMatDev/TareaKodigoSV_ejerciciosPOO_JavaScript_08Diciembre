/* Crear 3 Fuction que calcule 2 números enteros y muestre en consola el resultado de la suma, resta,
multiplicación y división. Puede utilizar parámetros o variables locales para asignar valores a los
números. */

function suma(num1, num2) {
    return num1 + num2;
}   

function resta(num1, num2) {
    return num1 - num2;
}

function multiplicacion(num1, num2) {
    return num1 * num2;
}

function division(num1, num2) {
    return num1 / num2;
}

console.log(suma(5, 5));
console.log(resta(5, 5));
console.log(multiplicacion(5, 5));
console.log(division(5, 5));